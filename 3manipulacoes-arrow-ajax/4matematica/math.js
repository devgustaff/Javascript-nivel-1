let round = Math.round(4.5); // arredonda para cima ou para baixo dependendo do número
console.log(round);

let floor = Math.floor(4.5); // arredonda sempre para baixo
console.log(floor);

let ceil = Math.ceil(4.5); // arredonda sempre para cima
console.log(ceil);

let min = Math.min(3, 5, 1) // retorna o menor número
console.log(min);

let max = Math.max(3, 5, 1, 8); // retorna o maior número
console.log(max);

// Gerando um número aleatório
let random = Math.round(Math.random() * 10);
console.log(random);